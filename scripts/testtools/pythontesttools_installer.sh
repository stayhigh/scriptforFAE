#  python testing tools 
pip install nose #  nose extends unittest to make testing easier.
pip install pytest #  py.test is a no-boilerplate alternative to Python’s standard unittest module.
pip install tox #  tox is a tool for automating test environment management and testing against multiple interpreter configurations
pip install unittest2 #  unittest2 is a backport of Python 2.7’s unittest module which has an improved API and better assertions over the one available in previous versions of Python.
pip install mock #  unittest.mock is a library for testing in Python. As of Python 3.3, it is available in the standard library.
